public class LogUtil {

    public static void registrar(String mensagem) {
        System.out.println("[LOG] " + mensagem);
    }
}
//LogUtil é uma classe para ajudar a escrever mensagens de log.
//O método registrar imprime mensagens no console com um prefixo [LOG].
//Serve para ajudar a monitorar o que está acontecendo no seu programa.